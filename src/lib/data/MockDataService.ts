// eslint-disable-next-line @typescript-eslint/no-namespace
export namespace MockDataService {
  export async function getMockData(exchange: Exchange): Promise<MarketSecurity[]> {
    // Simulate network delay
    await new Promise(resolve => setTimeout(resolve, 500 + Math.random() * 1000));

    const data = MOCK_SECURITIES[exchange];

    if (data.length === 0) {
      throw new Error(`No mock data available for ${exchange.toUpperCase()}`);
    }

    // Add some randomness to prices for demo purposes
    return data.map(security => ({
      ...security,
      priceLastSale: security.priceLastSale * (0.95 + Math.random() * 0.1),
      priceChangePct: -5 + Math.random() * 10,
      volume: Math.floor(security.volume * (0.8 + Math.random() * 0.4)),
    }));
  }
}
